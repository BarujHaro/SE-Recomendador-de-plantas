mostrar_caracteristicas(Planta) :-
    planta(Planta),  % Verifica que la planta exista en la base de conocimiento
    nl, write('=== CARACTERISTICAS DE '), write(Planta), write(' ==='), nl, nl,
    
    % Nombre cientifico
    (nombreCientifico(Planta, Cientifico) -> 
        write('Nombre cientifico: '), write(Cientifico), nl
    ; 
        write('Nombre cientifico: no disponible'), nl
    ),
    
    % Cantidad de riego
    cantidadDeRiego(Planta, Riego),
    write('Cantidad de riego: '), write(Riego), nl,
    
    % Frecuencia de riego
    frecuenciaDeriego(Planta, FrecRiego),
    write('Frecuencia de riego: '), write(FrecRiego), nl,
    
    % Espacio necesario
    espacioDisponible(Planta, Espacio),
    write('Espacio necesario: '), write(Espacio), nl,
    
    % Facilidad de cuidado
    facilCuidado(Planta, Facilidad),
    write('Facilidad de cuidado: '), write(Facilidad), nl,
    
    % Seguridad para mascotas
    seguraParaMascotas(Planta, Mascotas),
    write('Segura para mascotas: '), write(Mascotas), nl,
    
    % Tipo de luz requerida
    luz(Planta, Luz),
    write('Tipo de luz: '), write(Luz), nl,
    
    % Humedad ambiental
    humedad(Planta, Humedad),
    write('Requiere humedad: '), write(Humedad), nl,
    
    % Temperatura ideal
    temperatura(Planta, Temp),
    write('Temperatura ideal: '), write(Temp), nl,
    
    % Tipo de fertilizante
    tipoFertilizante(Planta, Fertilizante),
    write('Tipo de fertilizante: '), write(Fertilizante), nl,
    
    % Frecuencia de fertilizacion
    frecuenciaFertilizacion(Planta, FrecFert),
    write('Frecuencia de fertilizacion: '), write(FrecFert), nl,
    
    nl, write('=== FIN DE LAS CARACTERISTICAS ==='), nl.






